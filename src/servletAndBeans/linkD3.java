package servletAndBeans;

public class linkD3 {
	
	private int source;
	private int target;
	private int weight;
	
	public linkD3(int source, int target, int weight) {
		super();
		this.source = source;
		this.target = target;
		this.weight = weight;
	}

	public int getSource() {
		return source;
	}

	public void setSource(int source) {
		this.source = source;
	}

	public int getTarget() {
		return target;
	}

	public void setTarget(int target) {
		this.target = target;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
	
	
	
	

}
