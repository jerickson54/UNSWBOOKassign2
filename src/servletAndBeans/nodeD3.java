package servletAndBeans;

public class nodeD3 {

	private String name;
	private int group;
	
	
	
	
	
	public nodeD3(String name, int group) {
		super();
		this.name = name;
		this.group = group;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGroup() {
		return group;
	}
	public void setGroup(int group) {
		this.group = group;
	}
	
	
}
